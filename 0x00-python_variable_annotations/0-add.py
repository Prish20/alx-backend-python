#!/usr/bin/env python3
"""
This module contains a function that adds two floats.
"""


def add(a: float, b: float) -> float:
    """
    Adds two float numbers together.

    Args:
        a (float): The first float number.
        b (float): The second float number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
